	Schwarzenbach spielt an auf den Roman
	<span data-type="bibl" data-key="VKTJ5RJ7">Ferien vom Ich</span> (1915) von
	<span data-type="person" data-key="person_0618">Paul Keller</span> bzw. seine erste
	<span data-type="bibl" data-key="6FW9GPED">Verfilmung</span> (1934); der Stoff handelt von einer fiktionalen
	Therapiemethode, in der Patient:innen für eine gewisse Zeit an einem ländlichen Rückzugsort die eigene
	Identität aufgegeben. Er nimmt moderne Formen therapeutischer Auszeit-Formate vorweg. Im vorliegenden
	Text hat die Identitätsaufgabe einen politischen Hintersinn: Das berichtende Ich enthält sich seiner
	'politischen Identität' – an der Oberfläche zu Ferienzwecken, implizit als ironische Haltung, dass sein
	eigentliches Ich die beschriebene Realität keineswegs unkritisch sieht. Im Folgenden entkommt es denn
	auch nicht der Politik.
